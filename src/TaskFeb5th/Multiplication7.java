package TaskFeb5th;

public class Multiplication7 {
    public static void main(String[] args) {

        int number = 7;
        int multiply= 0;
        for (int i = 1; i <= 10 ; i++) {
            multiply = number * i;
            System.out.println("7 * " + i +" = "+ multiply);
        }

    }
}
